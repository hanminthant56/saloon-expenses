function changeLanguage(lang) {
    // Update all elements with data-i18n attribute
    const elements = document.querySelectorAll('[data-i18n]');
    
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            // If it's an input with a placeholder
            if (element.tagName === 'INPUT' && element.hasAttribute('placeholder')) {
                element.placeholder = translations[lang][key];
            } else {
                // For regular text elements
                element.textContent = translations[lang][key];
            }
        }
    });

    // Optional: Save preference to localStorage
    localStorage.setItem('preferred_language', lang);
}

// Initialize with default language or saved language on load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferred_language') || 'my'; // Default to Myanmar as per your file
    const selector = document.getElementById('lang-selector');
    if(selector) {
        selector.value = savedLang;
        changeLanguage(savedLang);
    }
});